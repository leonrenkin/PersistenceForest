"""Sampling utilities for embedded planar graph benchmark examples."""

from __future__ import annotations

from typing import Any

import numpy as np


def _as_nodes(nodes: Any) -> np.ndarray:
    """Return validated node coordinates as a float array."""
    nodes_array = np.asarray(nodes, dtype=np.float64)
    if nodes_array.ndim != 2 or nodes_array.shape[1] != 2:
        raise ValueError("graph.nodes must have shape (n_nodes, 2)")
    if not np.all(np.isfinite(nodes_array)):
        raise ValueError("graph.nodes must contain only finite coordinates")
    return nodes_array


def _as_edges(edges: Any, n_nodes: int) -> np.ndarray:
    """Return validated edge endpoint indices as an int array."""
    edges_array = np.asarray(edges, dtype=np.int64)
    if edges_array.size == 0:
        return np.empty((0, 2), dtype=np.int64)
    if edges_array.ndim != 2 or edges_array.shape[1] != 2:
        raise ValueError("graph.edges must have shape (n_edges, 2)")
    if np.any(edges_array < 0) or np.any(edges_array >= n_nodes):
        raise ValueError("graph.edges contains node indices outside graph.nodes")
    return edges_array


def _resolve_target_spacing(
    target_spacing: float | None,
    points_per_unit: float | None,
) -> float:
    """Resolve exactly one sampling density control to a target spacing."""
    if target_spacing is None and points_per_unit is None:
        raise ValueError("provide exactly one of target_spacing or points_per_unit")
    if target_spacing is not None and points_per_unit is not None:
        raise ValueError("provide exactly one of target_spacing or points_per_unit")

    if target_spacing is not None:
        spacing = float(target_spacing)
        if spacing <= 0.0 or not np.isfinite(spacing):
            raise ValueError("target_spacing must be a positive finite number")
        return spacing

    density = float(points_per_unit)
    if density <= 0.0 or not np.isfinite(density):
        raise ValueError("points_per_unit must be a positive finite number")
    return 1.0 / density


def sample_planar_graph(
    graph,
    *,
    target_spacing: float | None = None,
    points_per_unit: float | None = None,
    return_metadata: bool = False,
):
    """Sample points along graph edges while including every graph vertex.

    Interior edge samples are generated by splitting each edge into
    ``max(1, round(edge_length / target_spacing))`` segments. This keeps
    spacing close to the requested target on each edge while respecting graph
    vertices as fixed sample locations.

    Parameters
    ----------
    graph:
        Any object with ``nodes`` and ``edges`` attributes.
    target_spacing:
        Desired spacing between adjacent samples along an edge.
    points_per_unit:
        Alternative density control. Internally converted to
        ``target_spacing = 1 / points_per_unit``.
    return_metadata:
        If true, return ``(points, metadata)``. Metadata contains ``edge_index``
        and ``t`` arrays. Vertex samples use ``edge_index = -1`` and ``t = nan``.

    Returns
    -------
    numpy.ndarray or tuple[numpy.ndarray, dict[str, numpy.ndarray]]
        Sample coordinates, optionally with source metadata.
    """
    spacing = _resolve_target_spacing(target_spacing, points_per_unit)
    nodes = _as_nodes(graph.nodes)
    edges = _as_edges(graph.edges, nodes.shape[0])

    points: list[np.ndarray] = [node.copy() for node in nodes]
    edge_indices: list[int] = [-1] * nodes.shape[0]
    edge_parameters: list[float] = [np.nan] * nodes.shape[0]

    for edge_index, (u, v) in enumerate(edges):
        start = nodes[int(u)]
        end = nodes[int(v)]
        vector = end - start
        length = float(np.linalg.norm(vector))
        n_segments = max(1, int(round(length / spacing)))

        for segment_index in range(1, n_segments):
            t = segment_index / n_segments
            points.append(start + t * vector)
            edge_indices.append(int(edge_index))
            edge_parameters.append(float(t))

    sampled = np.asarray(points, dtype=np.float64)

    if not return_metadata:
        return sampled

    metadata = {
        "edge_index": np.asarray(edge_indices, dtype=np.int64),
        "t": np.asarray(edge_parameters, dtype=np.float64),
    }
    return sampled, metadata
